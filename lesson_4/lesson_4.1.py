list_1 = [9, 0, 7, 31, 0, 45, 0, 45, 0, 45, 0, 0, 96, 0]

#рахуємо кількість ітерацій
count_of_iteration = list_1.count(0)

#додаємо умову, якщо останній елемент списку 0, зменшуємо ітерацію на 1
if not list_1[-1]:
    count_of_iteration -= 1

#викликаємо цикл у якому по-перше видаляємо 0 зі списку, по-друге підставляємо 0 в кінець списку
for i in range(0, count_of_iteration):
    list_1.remove(0)
    list_1.append(0)

#виводимо список з переміщеними 0 в кінець списку
print(list_1)
